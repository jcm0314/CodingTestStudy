import java.util.*;
import java.io.*;

public class Main {
    public static void main(String[] args) throws Exception {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int t = Integer.parseInt(br.readLine());
        for(int a=1; a <= t; a++) {
            System.out.println("Hello World, Judge "+a+"!");
        }
    }
}